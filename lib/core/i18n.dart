  static String _normalize(String code) {
    if (code.startsWith('vi')) return 'vi';
    return 'en';
  }

  static String tr(BuildContext context, String key) {
    final lang = _normalize(Localizations.localeOf(context).languageCode);
    return _dict[lang]?[key] ?? _dict['en']![key] ?? key;
  }

  static String trByCode(String code, String key) {
    String lang;
    if (code == 'system') {
      try {
        final sys = ui.PlatformDispatcher.instance.locale.languageCode;
        lang = _normalize(sys);
      } catch (_) {
        lang = 'en';
      }
    } else {
      lang = _normalize(code);
    }
    return _dict[lang]?[key] ?? _dict['en']![key] ?? key;
  }
